package Day26.java;
public class codingday26 {
    public static void main(String[] args) {
        
        int y = 8;
        System.out.println("Nilai awal y = " + y);
        y += 4;
        System.out.println("Setelah y += 4, nilai y = " + y);
        y -= 3;
        System.out.println("Setelah y -= 3, nilai y = " + y);
        y *= 5;
        System.out.println("Setelah y *= 5, nilai y =" + y);
        y %= 6;
        System.out.println("Setelah y %= 6, nilai y =" + y);
        
        
    }
    
}
